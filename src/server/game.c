// Tengo questo controllo separato perche compare in molti punti della logica:
// movimento, rivelazione muri e costruzione delle mappe serializzate.
static int is_inside_map(int x, int y) {
    return x >= 0 && x < MAP_W && y >= 0 && y < MAP_H;
}

// Tutta la serializzazione delle mappe e delle liste passa da buffer testuali:
// qui aggiungo testo in coda senza rischiare di uscire dai limiti.
static int append_text(char *out, size_t out_size, const char *text) {
    size_t used_length;
    int written;

    if (out_size == 0) {
        return -1;
    }
    used_length = strlen(out);
    if (used_length >= out_size) {
        return -1;
    }
    written = snprintf(out + used_length, out_size - used_length, "%s", text);
    if (written < 0 || (size_t)written >= out_size - used_length) {
        return -1;
    }
    return 0;
}

// Genera l'identificatore stabile dello slot giocatore, ad esempio P0.
static void build_player_symbol(size_t slot, char *out, size_t out_size) {
    snprintf(out, out_size, "P%zu", slot);
}

// Espande l'array dinamico dei giocatori quando non basta piu.
static int game_reserve_players(game_t *game, size_t needed) {
    player_t *new_players;
    size_t old_capacity = game->player_capacity;
    size_t new_capacity = old_capacity == 0 ? INITIAL_PLAYERS_CAPACITY : old_capacity;

    while (new_capacity < needed) {
        new_capacity *= 2;
    }
    if (new_capacity == old_capacity) {
        return 0;
    }
    new_players = realloc(game->players, new_capacity * sizeof(*new_players));
    if (new_players == NULL) {
        return -1;
    }
    memset(new_players + old_capacity, 0, (new_capacity - old_capacity) * sizeof(*new_players));
    game->players = new_players;
    game->player_capacity = new_capacity;
    return 0;
}

// Per lo spawn faccio due passaggi: nel primo conto quante celle valide esistono,
// nel secondo ne scelgo una casualmente. E' semplice e non richiede strutture extra.
static int find_free_spawn(const game_t *game, int *x, int *y) {
    int map_y;
    int map_x;
    size_t player_index;
    int occupied;
    int free_count = 0;
    int chosen;

    for (map_y = 0; map_y < MAP_H; ++map_y) {
        for (map_x = 0; map_x < MAP_W; ++map_x) {
            if (game->wall[map_y][map_x]) {
                continue;
            }
            occupied = 0;
            for (player_index = 0; player_index < game->player_count; ++player_index) {
                if (game->players[player_index].active &&
                    game->players[player_index].x == map_x &&
                    game->players[player_index].y == map_y) {
                    occupied = 1;
                    break;
                }
            }
            if (!occupied) {
                free_count++;
            }
        }
    }

    if (free_count == 0) {
        return -1;
    }

    chosen = rand() % free_count;
    for (map_y = 0; map_y < MAP_H; ++map_y) {
        for (map_x = 0; map_x < MAP_W; ++map_x) {
            if (game->wall[map_y][map_x]) {
                continue;
            }
            occupied = 0;
            for (player_index = 0; player_index < game->player_count; ++player_index) {
                if (game->players[player_index].active &&
                    game->players[player_index].x == map_x &&
                    game->players[player_index].y == map_y) {
                    occupied = 1;
                    break;
                }
            }
            if (!occupied) {
                if (chosen == 0) {
                    *x = map_x;
                    *y = map_y;
                    return 0;
                }
                chosen--;
            }
        }
    }

    return -1;
}

// Lo stato iniziale della partita e volutamente leggero: azzero i proprietari
// e scelgo una delle mappe di muri gia compilate nel sorgente.
void game_init(game_t *game) {
    int y;
    int x;

    srand((unsigned int)time(NULL));
    for (y = 0; y < MAP_H; ++y) {
        for (x = 0; x < MAP_W; ++x) {
            game->owner[y][x] = -1;
        }
    }

    memcpy(game->wall, WALL_MAPS[rand() % MAP_COUNT], sizeof(game->wall));
}

// Libera le risorse dinamiche dello stato di gioco.
void game_free(game_t *game) {
    free(game->players);
    memset(game, 0, sizeof(*game));
}

// Quando un utente entra in partita provo prima a riutilizzare un suo vecchio
// slot, poi uno slot libero, e solo se serve ne creo uno nuovo.
int game_add_player(game_t *game, const char *nickname) {
    size_t i;
    size_t slot = 0;
    int found_slot = 0;
    int x = 0;
    int y = 0;

    if (game_find_player(game, nickname) >= 0 || find_free_spawn(game, &x, &y) != 0) {
        return -1;
    }
    for (i = 0; i < game->player_count; ++i) {
        if (game->players[i].used && strcmp(game->players[i].nickname, nickname) == 0) {
            slot = i;
            found_slot = 1;
            break;
        }
    }
    if (!found_slot) {
        for (i = 0; i < game->player_count; ++i) {
            if (!game->players[i].used) {
                slot = i;
                found_slot = 1;
                break;
            }
        }
    }
    if (!found_slot) {
        slot = game->player_count;
        if (game_reserve_players(game, game->player_count + 1) != 0) {
            return -1;
        }
        game->player_count++;
    }

    if (!game->players[slot].used) {
        memset(&game->players[slot], 0, sizeof(game->players[slot]));
        strncpy(game->players[slot].nickname, nickname, NICK_MAX);
        game->players[slot].nickname[NICK_MAX] = '\0';
        build_player_symbol(slot, game->players[slot].symbol, sizeof(game->players[slot].symbol));
        game->players[slot].used = 1;
    }
    game->players[slot].active = 1;
    game->players[slot].x = x;
    game->players[slot].y = y;
    game->owner[y][x] = slot;
    reveal_walls_around_player(game, slot);
    return (int)slot;
}

// Segna il giocatore come offline senza cancellare lo storico dei territori.
void game_remove_player(game_t *game, int player_id) {
    if (player_id >= 0 && (size_t)player_id < game->player_count) {
        game->players[player_id].active = 0;
    }
}

// Cerca un giocatore attualmente online dato il nickname.
int game_find_player(const game_t *game, const char *nickname) {
    size_t i;
    for (i = 0; i < game->player_count; ++i) {
        if (game->players[i].active && strcmp(game->players[i].nickname, nickname) == 0) {
            return (int)i;
        }
    }
    return -1;
}

// La "scoperta" dei muri resta locale al giocatore: aggiorno solo la piccola
// area intorno alla sua posizione corrente.
static void reveal_walls_around_player(game_t *game, int player_id) {
    int dx;
    int dy;
    int x;
    int y;
    player_t *p;

    if (player_id < 0 || (size_t)player_id >= game->player_count || !game->players[player_id].active) {
        return;
    }
    p = &game->players[player_id];
    for (dy = -1; dy <= 1; ++dy) {
        for (dx = -1; dx <= 1; ++dx) {
            x = p->x + dx;
            y = p->y + dy;
            if (is_inside_map(x, y) && game->wall[y][x]) {
                p->discovered_walls[y][x] = 1;
            }
        }
    }
}

// Qui concentro tutte le regole del movimento: bordi, muri, celle occupate e
// conquista del territorio quando il passo va a buon fine.
int game_move(game_t *game, int player_id, direction_t dir) {
    int nx;
    int ny;
    size_t i;
    player_t *p;

    if (player_id < 0 || (size_t)player_id >= game->player_count || !game->players[player_id].active) {
        return -1;
    }
    p = &game->players[player_id];
    nx = p->x;
    ny = p->y;
    if (dir == DIR_UP) {
        --ny;
    } else if (dir == DIR_DOWN) {
        ++ny;
    } else if (dir == DIR_LEFT) {
        --nx;
    } else if (dir == DIR_RIGHT) {
        ++nx;
    }

    if (!is_inside_map(nx, ny)) {
        return -2;
    }
    if (game->wall[ny][nx]) {
        p->discovered_walls[ny][nx] = 1;
        reveal_walls_around_player(game, player_id);
        return -3;
    }
    for (i = 0; i < game->player_count; ++i) {
        if ((int)i != player_id && game->players[i].active &&
            game->players[i].x == nx && game->players[i].y == ny) {
            return -4;
        }
    }
    p->x = nx;
    p->y = ny;
    game->owner[ny][nx] = player_id;
    reveal_walls_around_player(game, player_id);
    return 0;
}

// Aggiunge una cella alla codifica testuale della mappa separata da virgole.
static int append_cell(char *out, size_t out_size, int *first_cell, const char *cell) {
    if (!*first_cell) {
        if (append_text(out, out_size, ",") != 0) {
            return -1;
        }
    }
    if (append_text(out, out_size, cell) != 0) {
        return -1;
    }
    *first_cell = 0;
    return 0;
}

// La vista locale e una finestra centrata sul giocatore. Fuori dalla mappa
// uso '~', sulla sua cella uso '@', i muri scoperti sono '#', il resto e territorio.
int game_build_local_map(const game_t *game, int player_id, char *out, size_t out_size) {
    int y;
    int x;
    int map_x;
    int map_y;
    int start_x;
    int start_y;
    int owner;
    const player_t *p;

    if (out_size == 0) {
        return -1;
    }
    out[0] = '\0';
    if (player_id < 0 || (size_t)player_id >= game->player_count || !game->players[player_id].active) {
        return -1;
    }
    p = &game->players[player_id];
    start_x = p->x - LOCAL_VIEW_W / 2;
    start_y = p->y - LOCAL_VIEW_H / 2;

    for (y = 0; y < LOCAL_VIEW_H; ++y) {
        int first_cell = 1;
        if (y > 0) {
            if (append_text(out, out_size, "/") != 0) {
                return -1;
            }
        }
        for (x = 0; x < LOCAL_VIEW_W; ++x) {
            map_x = start_x + x;
            map_y = start_y + y;
            if (!is_inside_map(map_x, map_y)) {
                if (append_cell(out, out_size, &first_cell, "~") != 0) {
                    return -1;
                }
            } else if (p->x == map_x && p->y == map_y) {
                if (append_cell(out, out_size, &first_cell, "@") != 0) {
                    return -1;
                }
            } else if (p->discovered_walls[map_y][map_x]) {
                if (append_cell(out, out_size, &first_cell, "#") != 0) {
                    return -1;
                }
            } else {
                owner = game->owner[map_y][map_x];
                if (append_cell(out, out_size, &first_cell,
                                owner >= 0 && (size_t)owner < game->player_count && game->players[owner].used
                                    ? game->players[owner].symbol
                                    : ".") != 0) {
                    return -1;
                }
            }
        }
    }
    return 0;
}

// La mappa globale e piu "povera" apposta: mostra solo la proprieta delle celle
// e non deve rivelare l'informazione privata sui muri.
int game_build_global_map(const game_t *game, char *out, size_t out_size) {
    int y;
    int x;
    int owner;

    if (out_size == 0) {
        return -1;
    }
    out[0] = '\0';
    for (y = 0; y < MAP_H; ++y) {
        int first_cell = 1;
        if (y > 0) {
            if (append_text(out, out_size, "/") != 0) {
                return -1;
            }
        }
        for (x = 0; x < MAP_W; ++x) {
            owner = game->owner[y][x];
            if (append_cell(out, out_size, &first_cell,
                            owner >= 0 && (size_t)owner < game->player_count && game->players[owner].used
                                ? game->players[owner].symbol
                                : ".") != 0) {
                return -1;
            }
        }
    }
    return 0;
}

// Questa lista e pensata per il client: gli basta una stringa compatta con
// nickname, simbolo e coordinate per mostrare gli esploratori attivi.
int game_build_positions(const game_t *game, char *out, size_t out_size) {
    size_t i;
    char tmp[128];

    if (out_size == 0) {
        return -1;
    }
    out[0] = '\0';
    for (i = 0; i < game->player_count; ++i) {
        if (game->players[i].active) {
            if (out[0] != '\0') {
                if (append_text(out, out_size, ",") != 0) {
                    return -1;
                }
            }
            snprintf(tmp, sizeof(tmp), "%s:%s:%d:%d",
                     game->players[i].nickname,
                     game->players[i].symbol,
                     game->players[i].x,
                     game->players[i].y);
            if (append_text(out, out_size, tmp) != 0) {
                return -1;
            }
        }
    }
    if (out[0] == '\0') {
        if (append_text(out, out_size, "-") != 0) {
            return -1;
        }
    }
    return 0;
}

static int *compute_scores(const game_t *game) {
    // Il punteggio e semplicemente il numero di celle possedute. Tengo questo
    // conteggio in un helper unico per riusarlo sia nel riepilogo sia nel vincitore.
    int *scores;
    int y;
    int x;

    scores = calloc(game->player_count == 0 ? 1 : game->player_count, sizeof(*scores));
    if (scores == NULL) {
        return NULL;
    }
    for (y = 0; y < MAP_H; ++y) {
        for (x = 0; x < MAP_W; ++x) {
            if (game->owner[y][x] >= 0 &&
                (size_t)game->owner[y][x] < game->player_count &&
                game->players[game->owner[y][x]].used) {
                scores[game->owner[y][x]]++;
            }
        }
    }
    return scores;
}

// Qui prendo i punteggi gia calcolati e li serializzo nel formato testuale
// che il client usa nel riepilogo finale.
int game_build_scores(const game_t *game, char *out, size_t out_size) {
    int *scores;
    size_t i;
    char tmp[96];

    if (out_size == 0) {
        return -1;
    }
    scores = compute_scores(game);
    if (scores == NULL) {
        snprintf(out, out_size, "-");
        return -1;
    }
    out[0] = '\0';
    for (i = 0; i < game->player_count; ++i) {
        if (game->players[i].used) {
            if (out[0] != '\0') {
                if (append_text(out, out_size, ",") != 0) {
                    free(scores);
                    return -1;
                }
            }
            snprintf(tmp, sizeof(tmp), "%s:%d", game->players[i].nickname, scores[i]);
            if (append_text(out, out_size, tmp) != 0) {
                free(scores);
                return -1;
            }
        }
    }
    if (out[0] == '\0') {
        if (append_text(out, out_size, "-") != 0) {
            free(scores);
            return -1;
        }
    }
    free(scores);
    return 0;
}

// Il vincitore e semplicemente il giocatore con il punteggio piu alto nel momento
// in cui la partita termina.
int game_winner(const game_t *game, char *nickname, size_t nickname_size, int *score) {
    int *scores;
    int best = -1;
    int best_score = -1;
    size_t i;

    scores = compute_scores(game);
    if (scores == NULL) {
        snprintf(nickname, nickname_size, "-");
        *score = 0;
        return -1;
    }
    for (i = 0; i < game->player_count; ++i) {
        if (game->players[i].used && scores[i] > best_score) {
            best = (int)i;
            best_score = scores[i];
        }
    }
    if (best < 0) {
        snprintf(nickname, nickname_size, "-");
        *score = 0;
        free(scores);
        return -1;
    }
    snprintf(nickname, nickname_size, "%s", game->players[best].nickname);
    *score = best_score;
    free(scores);
    return 0;
}
